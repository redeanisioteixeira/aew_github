                <p class="text-justify">
                Este é um espaço de compartilhamento de conteúdos educacionais digitais produzidos para/pela comunidade escolar. São softwares, áudios, vídeos, animações, e outros conteúdos multimídia, que deverão atender às necessidades de estudantes, professores e toda a comunidade, na medida de suas necessidades de formação. O objetivo é que esta seção seja composta por produções de diversas áreas do conhecimento de forma interdisciplinar. Temos assim um repositório de materiais didáticos produzidos pela Secretaria da Educação, universidades e pela comunidade escolar. Esse recurso pode potencializar a construção de uma rede colaborativa de produção.
                </p>
