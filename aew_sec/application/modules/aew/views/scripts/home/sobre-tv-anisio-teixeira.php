        <p>A <b>TV Anísio Teixeira</b> conta com o trabalho de uma equipe multidisciplinar da Rede Estadual de Ensino, para produzir conteúdos audiovisuais educacionais, através do olhar de professores formados para compreender, criar e realizar vídeos direcionados para a comunidade escolar. Com abordagem lúdica e pedagógica, o conteúdo produzido trabalha temas transversais, interligando as diversas áreas do conhecimento. Preparados dentro dos padrões de exibição de TV e WEB, os vídeos servem como disparadores para atividades em classe, contribuindo com a maior dinamização da aula e auxiliando na preparação do estudante para os desafios de sua formação. Esse trabalho pretende fomentar a compreensão do universo de conhecimento, de maneira ampla e fundamentada, exemplificando o conteúdo com elementos do cotidiano do estudante e possibilitando seu diálogo com o mundo, ao mesmo tempo que preserva sua identidade regional. Todos os programas e campanhas estão catalogados também no Ambiente Educacional Web, onde é possível fazer o download dos vídeos.</p>
