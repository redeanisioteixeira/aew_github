        <h5><b>Bahia: pessoas, lugares, culturas e olhares. Exposição fotográfica do Ambiente Educacional Web</b></h5>

        <div class="text-justify">
            
            <p>No intuito de criar um ambiente que considere diferentes olhares e perspectivas sobre os patrimônios materiais e imateriais do nosso Estado, a tela de acesso ao Ambiente Educacional Web foi transformada em um espaço de exposição fotográfica. Neste local, será possível apresentar variados olhares fotográficos sobre as diversas regiões da Bahia, suas pessoas e seus lugares.</p>
           
            <blockquote class="direita fa fa-quote-right fa-3x col-lg-6 col-md-6 col-sm-12 col-xs-12 link-preto">
                <span>A fotografia sempre esteve presente na vida do educador e geógrafo Peterson Azevedo, mas foi a partir do ano de 2008 que ela virou mais que um hobby, tornou-se uma vocação.</span>
                <span>A sensibilidade do seu olhar transforma o registro fotográfico numa poesia visual, uma verdadeira obra de arte. Os aspectos técnicos de luz, composição e cor são, frequentemente, experimentados e ressignificados por esse fotógrafo</span>
            </blockquote>

            <p>Caso seja fotógrafo e considere que sua região ou cultura ainda não estejam representados nesta exposição entre em contato através do <a class="faleConosco" data-toggle="modal" data-target="#modalGeral" href="/home/faleconosco"><b>fale conosco</b></a> para ampliarmos este acervo e oferecer subsídios para os professores e estudantes contarem com mais material para conhecer nossas “Bahias”.</p>
            <p>Temos a felicidade de começar esta exposição conhecendo um pouco do olhar fotográfico de um de nossos professores da rede pública baiana, <b>Peterson Azevedo</b> (acesse à <a href="https://www.facebook.com/pages/Peterson-Azevedo-Photography/452834521460207" target="_blank"><b>galeria de fotos</b></a>).</p>
